{$CLEO .csi}
// ================================================================
// GTA SA Android - NoClip Mod
// الملف: noclip.csi
// يظهر في قائمة CLEO عند هبوط الشاشة
// تفعيله من القائمة يشغّل وضع NoClip
// ================================================================
// التحكم أثناء NoClip:
//   الجويستيك الأيسر  → تحرك أمام/خلف/يمين/يسار
//   لمس النقطة 2 (أعلى الشاشة وسط) → الصعود لفوق
//   لمس النقطة 8 (أسفل الشاشة وسط) → النزول لأسفل
//   لمس النقطة 4 (وسط يسار)  → سرعة بطيئة
//   لمس النقطة 6 (وسط يمين)  → سرعة سريعة
//   لمس النقطة 5 (وسط الشاشة) → سرعة عادية
// ================================================================

0000: NOP

// ── التهيئة ──────────────────────────────────────────────────
// 0@ = سرعة الحركة (float)
// 10-12@ = إحداثيات اللاعب X Y Z
// 20-23@ = حالة الجويستيك

:INIT
  float 0@ = 0.3           // السرعة الافتراضية (عادية)

  0256: $PLAYER_CHAR = get_player_char 0

  // تعطيل التصادم فوراً
  038B: set_actor $PLAYER_CHAR collision 0

  // رسالة ترحيب
  0AD7: show_text_box "NoClip مفعّل | وسط=عادي | يسار=بطيء | يمين=سريع"

  wait 2000

// ── الحلقة الرئيسية ────────────────────────────────────────
:NOCLIP_LOOP
  wait 0

  0256: $PLAYER_CHAR = get_player_char 0

  // تأكد أن اللاعب حي
  if
    0106: actor $PLAYER_CHAR dead
  then
    jump @NOCLIP_LOOP
  end

  // تعطيل التصادم في كل إطار لضمان الاستمرارية
  038B: set_actor $PLAYER_CHAR collision 0

  // ── قراءة الإحداثيات الحالية ──
  0054: store_actor $PLAYER_CHAR XYZ_to 10@ 11@ 12@

  // ── قراءة الجويستيك ──
  // 20@ = محور X الأيسر (-128 إلى 127)
  // 21@ = محور Y الأيسر (-128 إلى 127)
  0AD3: 20@ 21@ 22@ 23@ = get_joystick_state

  // ── قراءة اتجاه اللاعب لحساب الحركة ──
  0172: 24@ = get_actor_heading $PLAYER_CHAR
  1C00: 25@ = to_radian 24@   // تحويل إلى راديان
  1C09: 26@ = cos 25@         // مكوّن X
  1C18: 27@ = sin 25@         // مكوّن Y

  // ── تعديل السرعة بالنقرات ──

  // نقطة 4 (يسار) = بطيء
  if
    00E1: key_pressed 3 touch_point 4
  then
    float 0@ = 0.08
    0AD7: show_text_box "السرعة: بطيئة"
    wait 600
  end

  // نقطة 5 (وسط) = عادي
  if
    00E1: key_pressed 3 touch_point 5
  then
    float 0@ = 0.3
    0AD7: show_text_box "السرعة: عادية"
    wait 600
  end

  // نقطة 6 (يمين) = سريع
  if
    00E1: key_pressed 3 touch_point 6
  then
    float 0@ = 0.8
    0AD7: show_text_box "السرعة: سريعة"
    wait 600
  end

  // ── حساب الحركة الأفقية من الجويستيك ──
  // تحويل قيم الجويستيك إلى float وضربها بالسرعة والاتجاه

  float 30@ = 21@         // المحور Y = أمام/خلف
  30@ *= 0.01
  30@ *= 0@

  float 31@ = 20@         // المحور X = يمين/يسار
  31@ *= 0.01
  31@ *= 0@

  // دلتا الحركة للأمام/الخلف
  float 32@ = 26@
  32@ *= 30@

  float 33@ = 27@
  33@ *= 30@

  // دلتا الحركة للجانبين (عمودي على الاتجاه)
  // cos(angle+90) = -sin(angle) , sin(angle+90) = cos(angle)
  float 34@ = 27@
  34@ *= 31@              // حركة يمين/يسار على X
  float 35@ = 26@
  35@ *= 31@              // حركة يمين/يسار على Y

  // تطبيق الحركة الأفقية
  10@ += 32@
  10@ -= 34@
  11@ += 33@
  11@ += 35@

  // ── الحركة الرأسية (فوق/تحت) ──

  // نقطة 2 (أعلى وسط) = الصعود
  if
    00E1: key_pressed 3 touch_point 2
  then
    float 36@ = 0@
    36@ *= 0.7
    12@ += 36@
  end

  // نقطة 8 (أسفل وسط) = النزول
  if
    00E1: key_pressed 3 touch_point 8
  then
    float 37@ = 0@
    37@ *= 0.7
    12@ -= 37@
  end

  // ── تطبيق الموقع الجديد ──
  0055: set_actor $PLAYER_CHAR XYZ_to 10@ 11@ 12@

  jump @NOCLIP_LOOP

// ── نهاية السكريبت ──
// عند إلغاء التفعيل من القائمة يتوقف تلقائياً
// وتُعاد فيزياء اللاعب
end
